# Estrutura-de-repetição
# var. int,n,lim

n = 1
lim = 10

print ('\n','*'*40)
while (n <= lim):
    print (f'\nContagem atual {n}')
    n = n+1

print ('\n','*'*40,'\n\nContagem encerrada\n\n','*'*40)


